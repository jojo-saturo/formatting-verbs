package main

import "fmt"

func main() {

	var Num int = 42

	fmt.Printf("%b \n", Num)
	fmt.Printf("%d \n", Num)
	fmt.Printf("%o \n", Num)
	fmt.Printf("%x \n", Num)
	fmt.Printf("%X \n", Num)
	fmt.Printf("%X \n", Num)
	fmt.Printf("%#x \n", Num)
	fmt.Printf("%4d \n", Num)
	fmt.Printf("%-4d \n", Num)
	fmt.Printf("%O4d \n", Num)
}
